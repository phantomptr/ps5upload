impl Language {
    pub fn from_code(code: &str) -> Self {
        match code {
            "zh-CN" => Language::ZhCn,
            "zh-TW" => Language::ZhTw,
            "fr" => Language::Fr,
            "es" => Language::Es,
            "ar" => Language::Ar,
            _ => Language::En,
        }
    }

    pub fn code(self) -> &'static str {
        match self {
            Language::En => "en",
            Language::ZhCn => "zh-CN",
            Language::ZhTw => "zh-TW",
            Language::Fr => "fr",
            Language::Es => "es",
            Language::Ar => "ar",
        }
    }

    pub fn label(self) -> &'static str {
        match self {
            Language::En => "English",
            Language::ZhCn => "简体中文",
            Language::ZhTw => "繁體中文",
            Language::Fr => "Français",
            Language::Es => "Español",
            Language::Ar => "العربية",
        }
    }
}

